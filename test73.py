#Use case of if..else syntax

if True:
    print("Hello")
else:
    print("Bye")

if False:
    print("Hello")
else:
    print("Bye")

if 100:
    print("Hello")
else:
    print("Bye")

if 0:
    print("Hello")
else:
    print("Bye")

if "Python":
    print("Python")
else:
    print("Java")

if "":
    print("Python")
else:
    print("Java")

if 10>2:
    print("Python")
else:
    print("Java")
    
